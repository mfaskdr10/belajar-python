# variabel adalah untuk menyimpan data

# assignment nilai
a = 10
b = 15
nilai_panjang = 1000
panjang = 10000

# pemanggilan pertama
print("Nilai a =", a)
print("Nilai b =", b)
print("Nilai nilai panjang =", nilai_panjang)
print("Nilai panjang =", panjang)

